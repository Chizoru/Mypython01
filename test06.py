# Operator ตัวดำเนินการ คือ เครื่องหมายที่ใช้ในการคประมวลผลข้อมูล
# 1.3 Arithmetic Operator เครื่องหมายคำนวณ 
# (+,-,*,/,% mod,// floor,** eponential,)
print(10 + 3)
print(10 - 3)
print(10 * 3)
print(10 / 3)
print(10 % 3) #หารเอาเศษ
print(10 // 3) #หารเอาส่วน
print(10 ** 3) 
print('+++++++++++++++++++++++++++++++++++')
# 2. Comparison Operator เครื่องหมายความเปรียบเทียบ (= , == , != , > , < , >= , <= )
print(10 == 3)
print(10 != 3)
print(10 > 3)
print(10 < 3)
print(10 >= 3)
print(10 <= 3)
print('Bangna' >= 'Bangpoo')
print('Thailand' < 'THAILAND')
print('+++++++++++++++++++++++++++++++++++')
# 3. Logical Operator เครื่องหมายตรรกะ
# (and , or , not)
print(not True)
print(not False)
print('---')
print(True and True) #true
print(True and False)
print(False and True)
print(False and False)
print('---')
print(True or True)
print(True or False)
print(False or True)
print(False or False) #false
print('+++++++++++++++++++++++++++++++++++')
